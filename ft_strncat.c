#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		y;

	i = 0;
	y = -1;
	if (n > 0)
	{
		while (s1[++y])
			;
		while (i < n && s2[i])
			s1[y++] = s2[i++];
		s1[y] = '\0';
	}
	return (s1);
}
