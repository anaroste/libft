#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*b2;
	size_t	i;

	b2 = b;
	i = 0;
	while (i < len)
	{
		b2[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
